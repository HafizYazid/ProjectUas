    public final void getData() {
        DefaultTableModel dtm = (DefaultTableModel) dataMenu.getModel();
        dtm.setRowCount(0);
        List<MenuGym> listMenu = conMenu.findAll();
        Object[] data = new Object[3];

        for (MenuGym dataMenu : listMenu) {
            data[0] = dataMenu.getIdKategori();
            data[1] = dataMenu.getJenisPengunjung();
            data[2] = dataMenu.getHarga();
            dtm.addRow(data);
        }
    }
    
