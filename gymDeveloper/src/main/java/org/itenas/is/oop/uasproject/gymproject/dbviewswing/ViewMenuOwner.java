    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int i = dataMenu.getSelectedRow();

    if (i == -1) {
        JOptionPane.showMessageDialog(btnUpdate, "Harap pilih salah satu data",
            "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        String idMenu = dataMenu.getModel().getValueAt(i, 0).toString();
        String oldNama = dataMenu.getModel().getValueAt(i, 1).toString();

        String HargaMenu = txtHarga.getText().trim();
        

        if (HargaMenu.isEmpty()) {
            JOptionPane.showMessageDialog(btnUpdate, "Semua input harus diisi.",
                "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        MenuGym updateMenu = new MenuGym();

        updateMenu.setHarga(Integer.parseInt(HargaMenu));
        
        conMenu.update(idMenu, updateMenu);

        JOptionPane.showMessageDialog(btnUpdate, "Data berhasil diUpdate");
        getData(); 
        clearData();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(btnUpdate, "Error: " + e.getMessage(),
            "Error", JOptionPane.ERROR_MESSAGE);
        }                                         

    }//GEN-LAST:event_btnUpdateActionPerformed

